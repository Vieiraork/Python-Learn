m = float(input('Digite o valor em metros: '))

print('O comprimento {}m é igual a {}km'.format(m, m/1000))
print('O comprimento {}m é igual a {}hm'.format(m, m/100))
print('O comprimento {}m é igual a {}dam'.format(m, m/10))
print('===============================================')
print('O comprimento {}m é igual a {}dm'.format(m, m*10))
print('O comprimento {}m é igual a {}cm'.format(m, m*100))
print('O comprimento {}m é igual a {}mm'.format(m, m*1000))
